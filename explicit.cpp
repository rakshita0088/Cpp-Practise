#include<iostream>
using namespace std;
int main(){
    float a=1.2;
    unsigned int b=66;
    float c=2.3;

    int d=(int)a;
    char e=(char)b;
    double f=(double)c;
    cout<<"converted integer value:"<<d<<endl;
    cout<<"converted char value:"<<e<<endl;
    cout<<"converted double value:"<<f<<endl;
};